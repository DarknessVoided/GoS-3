function lasthit()
	for i,minion in pairs(minionManager.objects) do
		if MINION_ENEMY == GetTeam(minion) then
			if ValidTarget(minion, myHeroRange) and GetDistance(myHero, minion) < myHeroRange then
				local minionInRange = minion
				if GetCurrentHP(minionInRange) - GetDamagePrediction(minionInRange,(GetDistance(myHero,minionInRange)/1200)*1000)  < CalcDamage(myHero,minion,GetBaseDamage(myHero)+GetBonusDmg(myHero),0) then
					AttackUnit(minionInRange)
				end
			end
		end
	end
end

function turretCheck(origin2)
	for id,i in pairs(turretTable) do
		_,n=creeps(GetOrigin(i),950,MINION_ALLY) 
		if GetDistance(origin2,GetOrigin(i))<950 and n<2 then
			print("Turr aggro inc")
			return true
		end
	end
end

function creeps(origin,range,Team)
	creepTable={}
	count=0
	for _,i in pairs(minionManager.objects) do
		if GetDistance(origin,GetOrigin(i))<range and GetTeam(i)==Team and IsObjectAlive(i) then
			creepTable[count]=i
			count=count+1
		end
	end
	return creepTable,count
end

function mode()
	if GetPercentHP(myHero)<50 then
		return "lowHP"
	elseif GetPercentMP(myHero)<5 then
		return "lowMP"
	elseif ClosestEnemy(GetOrigin(myHero)) then
		if GetDistance(GetOrigin(ClosestEnemy(GetOrigin(myHero))),GetOrigin(myHero))>1000 then
			return "safe"
		elseif GetPercentHP(ClosestEnemy(GetOrigin(myHero)))<5 then 
			return "kill"
		elseif GetDistance(GetOrigin(ClosestEnemy(GetOrigin(myHero))),GetOrigin(myHero))<200 then
			return "fight"
		end
	else
		return "normal"
	end
end


