function AF()
	cp={x=0,y=0,z=0}
	if GetGameTimer()>70 and not turretCheck(GetOrigin(myHero)) and not omwBase then
		if ClosestMinion(GetOrigin(myHero), MINION_ALLY) then
			DrawCircle(GetOrigin(myHero),250,0,3,0xffffff00)
			unit=ClosestMinion(GetOrigin(myHero), MINION_ALLY)
			if GetCurrentHP(unit)==540 then
				MoveToXYZ({x=7350, y=150, z=8000})
			end
			unitP=GetOrigin(unit)
			DrawCircle(unitP,75,0,3,0xffffff00)
			if GetDistance(unitP,GetOrigin(myHero))<750 then
				ctable,ccount=creeps(GetOrigin(myHero),750,MINION_ALLY)
					cp2=false
					for _,i in pairs(ctable) do
						cp=Vector(cp)+Vector(GetOrigin(i))
						cp2=true
					end
					if ccount~=nil and cp2 then 
						cp=cp/ccount
						DrawCircle(cp,300,0,3,0xffffff00)
						if GetDistance(GetOrigin(myHero),cp)>300 then
							MoveToXYZ(cp.x+math.random(-20,20),cp.y+math.random(-20,20),cp.z+math.random(-20,20))
						end
					end
				--MoveToXYZ(unitP)
			elseif GetDistance(unitP,GetOrigin(myHero))<3000 and not omwBase then
				MoveToXYZ(unitP)
			end
		end
	else
		if not omwBase then MoveToXYZ({x=7350, y=150, z=8000}) end
	end
end

function buyCheck(id)
	if GetItemSlot(myHero, id)>0 then
		return true
	end
	BuyItem(id)
	DelayAction(function ()
		if GetItemSlot(myHero, id)>0 then
			return true
		elseif GetItemSlot(myHero, id)==0 then
			return false
		end
	end,100)
end

function buyItems(itemTable)
	if lastTime+1<=GetGameTimer() and inFountain(myHero) then
	lastTime=GetGameTimer()
		while itemTable[buyPos]~=nil and GetItemSlot(myHero, itemTable[buyPos])>0 do
			buyPos=buyPos+1
		end
		if itemTable[buyPos]==nil then return end
		if GetItemSlot(myHero, itemTable[buyPos])==0 and buyCheck(itemTable[buyPos]) then
		else
			DelayAction( function() 
				subItem=nil
				for _,subItem in pairs(item[itemTable[buyPos]].from) do
					if subItem and tonumber(subItem) and GetItemSlot(myHero, itemTable[buyPos])==0 and GetItemSlot(myHero, subItem)==0 then
					buyCheck(subItem)
					end
				end
			end, 150)
		end
	end
